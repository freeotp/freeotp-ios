public struct IconMatch {
    var name: String
    var levDist: Int
}

public enum faIconType {
    case brands
    case solid
    case recommended
}

public class TokenIcon {
    let defaults = UserDefaults.standard

    public init() {}

    public let iconsBrand = Array(faIconsBrands.keys)
    public let iconsSolid = Array(faIconsSolid.keys)

    public func saveMapping(issuer: String, iconName: String, iconColor: String) {
        let iconDict = ["Name": iconName, "Color": iconColor]
        defaults.set(iconDict, forKey: issuer)
    }

    public func loadMapping(issuer: String) -> [String: String]? {
        guard let iconName = defaults.object(forKey: issuer) as? [String: String] else {
            return nil
        }
        return iconName
    }

    public func faIconExists(for iconName: String) -> (type: faIconType, name: String)!  {
        /* Drop any 'fa-' prefix from legacy FontAwesome library usage */
        let updated = iconName.lowercased().replacingOccurrences(of: "fa-", with: "")

        /* Use liberal search heuristics to handle names like 'Red Hat' or 'GitHub' */
        let newName = updated.lowercased().replacingOccurrences(of: " ", with: "")

        if !updated.isEmpty {
            /* Returns the type and updated string to the caller */
            if (faIconsBrands.keys.contains(newName)) {
                return (.brands, newName)
            } else if (faIconsSolid.keys.contains(newName)) {
                return (.solid, newName)
            }
        }
        return nil
    }

    public func getBrandColor(_ key: String) -> UIColor! {
        let brand = key.replacingOccurrences(of: " ", with: "")
        if let val = iconBGColorMapping[brand] {
            return UIColor(hexString: val)
        }

        return nil
    }

    public func getBrandColorHex(_ key: String) -> String! {
        let brand = key.replacingOccurrences(of: " ", with: "")
        if let val = iconBGColorMapping[brand] {
            return val
        }

        return nil
    }

    public func getBackgroundColor(name: String, uriColor: String = "") -> UIColor {
        // Use URI-provided color
        if uriColor != "" {
            return UIColor(hexString: uriColor)
        }
        // Use color from brand dictionary
        if let color = getBrandColor(name) {
            return color
        }
        // Fallback to predictable hash-based color
        return name == "" ? UIColor.systemGray : getBackgroundColorHash(name)
    }

    func sha256(data : Data) -> Data {
        var hash = [UInt8](repeating: 0,  count: Int(CC_SHA256_DIGEST_LENGTH))
        data.withUnsafeBytes {
            _ = CC_SHA256($0.baseAddress, CC_LONG(data.count), &hash)
        }
        return Data(hash)
    }

    public func getBackgroundColorHash(_ issuer: String) -> UIColor {
        let mydata = sha256(data: Data(issuer.utf8))
        let number = mydata.withUnsafeBytes( {
            $0.load(as: Int.self )
        })
        let idx = abs(number)
        let index = idx % backgroundColors.count
        return UIColor(hexString: backgroundColors[index])
    }

    public func getCustomIcon(issuer: String, size: CGSize =  CGSize(width: 30, height: 30)) -> IconData! {
        var icon = IconData()
        if let faName = issuerBrandMapping[issuer] {
            icon.iconImg = UIImage.fontAwesomeIcon(faName: faName, faType: .brands, size: size)
            icon.name = faName
            let color = iconBGColorMapping[faName]
            icon.bgColor = color ?? "#6F7171"
            return icon
        }

        if issuer.isEmpty {
            return nil
        }

        if let iconMapping = loadMapping(issuer: issuer) {
            if let faIcon = faIconExists(for: iconMapping["Name"]!) {
                icon.iconImg = UIImage.fontAwesomeIcon(faName: faIcon.name, faType: faIcon.type, size: size)
                icon.bgColor = iconMapping["Color"] ?? "#6F7171"
                icon.name = faIcon.name

                return icon
            }
        }
        return nil
    }

    public func getSuggestions(urlc: URLComponents, levDistMax: Int = 4, numSuggestions: Int = 3) -> [IconMatch] {
        var suggestedIcons = [IconMatch]()
        let URI = URIParameters()

        if let label = URI.getLabel(from: urlc) {
            for icon in faIconsBrands {
                let levDist = levDis(label.issuer, icon.key)
                if levDist < levDistMax {
                    let icon = IconMatch(name: icon.key, levDist: levDist)
                    suggestedIcons.append(icon)
                }
            }
        }

        suggestedIcons.sort { $0.levDist < $1.levDist }

        let remove = suggestedIcons.count - numSuggestions
        if remove > 0 {
            suggestedIcons.removeLast(remove)
        }

        return suggestedIcons
    }

    func levDis(_ w1: String, _ w2: String) -> Int {
        let empty = [Int](repeating:0, count: w2.count)
        var last = [Int](0...w2.count)

        for (i, char1) in w1.enumerated() {
            var cur = [i + 1] + empty
            for (j, char2) in w2.enumerated() {
                cur[j + 1] = char1 == char2 ? last[j] : Swift.min(last[j], last[j + 1], cur[j]) + 1
            }
            last = cur
        }
        return last.last!
    }

