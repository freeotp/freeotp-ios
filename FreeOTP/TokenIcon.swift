    public func getfaIconName(for brand: String) -> String! {
        let brand = brand.lowercased().replacingOccurrences(of: " ", with: "")

        if !brand.isEmpty {
            for icons in iconsBrand {
                if icons.name == brand.lowercased() {
                    return icons.value
                }
            }
        }
        return nil
    }

    public func getBrandColor(_ key: String) -> UIColor! {
        let brand = key.lowercased().replacingOccurrences(of: " ", with: "")
        if let val = iconBGColorMapping[brand] {
            return UIColor(hexString: val)
        }

        return nil
    }

    public func getBrandColorHex(_ key: String) -> String! {
        let brand = key.lowercased().replacingOccurrences(of: " ", with: "")
        if let val = iconBGColorMapping[brand] {
            return val
        }

        return nil
    }

    public func getBackgroundColor(name: String, uriColor: String = "") -> UIColor {
        // Use URI-provided color
        if uriColor != "" {
            return UIColor(hexString: uriColor)
        }
        // Use color from brand dictionary
        if let color = getBrandColor(name) {
            return color
        }
        // Fallback to predictable hash-based color
        return name == "" ? UIColor.systemGray : getBackgroundColorHash(name)
    }

    func sha256(data : Data) -> Data {
        var hash = [UInt8](repeating: 0,  count: Int(CC_SHA256_DIGEST_LENGTH))
        data.withUnsafeBytes {
            _ = CC_SHA256($0.baseAddress, CC_LONG(data.count), &hash)
        }
        return Data(hash)
    }

    public func getBackgroundColorHash(_ issuer: String) -> UIColor {
        let mydata = sha256(data: Data(issuer.utf8))
        let number = mydata.withUnsafeBytes( {
            $0.load(as: Int.self )
        })
        let idx = abs(number)
        let index = idx % backgroundColors.count
        return UIColor(hexString: backgroundColors[index])
    }

    public func getFontAwesomeIcon(issuer: String, _ size: CGSize = CGSize(width: 30, height: 30)) -> UIImage! {
        let faIconName = "fa-" + issuer.lowercased().replacingOccurrences(of: " ", with: "")
        if let iconName = FontAwesome(rawValue: faIconName) {
            return UIImage.fontAwesomeIcon(name: iconName, style: .brands, textColor: .white, size: size)
        }

        return nil
    }

    public func getFontAwesomeIcon(faName: String, faType: faIconType, size: CGSize = CGSize(width: 30, height: 30)) -> UIImage! {
        switch faType {
        case .brands:
            if let iconName = FontAwesome(rawValue: faName) {
                return UIImage.fontAwesomeIcon(name: iconName, style: .brands, textColor: .white, size: size)
            }
        case .solid:
            if let iconName = FontAwesome(rawValue: faName) {
                return UIImage.fontAwesomeIcon(name: iconName, style: .solid, textColor: .white, size: size)
            }
        case .recommended:
            if let iconName = FontAwesome(rawValue: faName) {
                return UIImage.fontAwesomeIcon(name: iconName, style: .brands, textColor: .white, size: size)
            }
        }

        return nil
    }

    public func getCustomIcon(issuer: String, size: CGSize =  CGSize(width: 30, height: 30)) -> IconData! {
        var icon = IconData()

        if let faName = issuerBrandMapping[issuer] {
            if let faIcon = FontAwesome(rawValue: faName) {
                icon.iconImg = UIImage.fontAwesomeIcon(name: faIcon, style: .brands, textColor: .white, size: size)
                icon.name = faName
                let color = iconBGColorMapping[faName]
                icon.bgColor = color ?? "#6F7171"
                
                return icon
            }
        }

        if issuer.isEmpty {
            return nil
        }

        if let iconMapping = loadMapping(issuer: issuer) {
            if faIconsBrands.values.contains(iconMapping["Name"]!) {
                if let iconName = FontAwesome(rawValue: iconMapping["Name"]!) {
                    icon.iconImg = UIImage.fontAwesomeIcon(name: iconName, style: .brands, textColor: .white, size: size)
                    icon.bgColor = iconMapping["Color"] ?? "#6F7171"
                    icon.name = iconMapping["Name"]!

                    return icon
                }
            }

            if faIconsSolid.values.contains(iconMapping["Name"]!) {
                if let iconName = FontAwesome(rawValue: iconMapping["Name"]!) {
                    icon.iconImg = UIImage.fontAwesomeIcon(name: iconName, style: .solid, textColor: .white, size: size)
                    icon.bgColor = iconMapping["Color"] ?? "#6F7171"
                    icon.name = iconMapping["Name"]!

                    return icon
                }
            }
        }
        return nil
    }

    public func getSuggestions(urlc: URLComponents, levDistMax: Int = 4, numSuggestions: Int = 3) -> [IconMatch] {
        var suggestedIcons = [IconMatch]()
        let URI = URIParameters()

        if let label = URI.getLabel(from: urlc) {
            for icons in iconsBrand {
                let levDist = levDis(label.issuer.lowercased(), icons.name)
                if levDist < levDistMax {
                    let icon = IconMatch(name: icons.value, levDist: levDist)
                    suggestedIcons.append(icon)
                }
            }
        }

        suggestedIcons.sort { $0.levDist < $1.levDist }

        let remove = suggestedIcons.count - numSuggestions
        if remove > 0 {
            suggestedIcons.removeLast(remove)
        }

        return suggestedIcons
    }

    func levDis(_ w1: String, _ w2: String) -> Int {
        let empty = [Int](repeating:0, count: w2.count)
        var last = [Int](0...w2.count)

        for (i, char1) in w1.enumerated() {
            var cur = [i + 1] + empty
            for (j, char2) in w2.enumerated() {
                cur[j + 1] = char1 == char2 ? last[j] : Swift.min(last[j], last[j + 1], cur[j]) + 1
            }
            last = cur
        }
        return last.last!
    }

